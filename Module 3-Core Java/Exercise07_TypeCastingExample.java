public class Exercise07_TypeCastingExample {

    public static void main(String[] args) {

        double d = 99.99;

        int i = (int)d;

        System.out.println("Double to Int = " + i);

        int num = 50;

        double value = (double)num;

        System.out.println("Int to Double = " + value);

    }
}
